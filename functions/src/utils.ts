export const operators = {
  ">": (a: number, b: number) => {
    return a > b;
  },
  "<": (a: number, b: number) => {
    return a < b;
  },
  "==": (a: number, b: number) => {
    return a == b;
  },
  ">=": (a: number, b: number) => {
    return a >= b;
  },
  "<=": (a: number, b: number) => {
    return a <= b;
  },
};
